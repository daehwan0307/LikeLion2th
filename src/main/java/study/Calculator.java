package study;

public class Calculator {

    public static int plus(int a,int b){

        return a+b;
    }
    public static int minus(int a,int b){

        return a-b;
    }
    public static int multiple(int a,int b){

        return a*b;
    }
    public static double divide(int a,int b){

        return (double)a/b;
    }
}
